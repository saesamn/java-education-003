public class BreakStatement { // 클래스 정의 시작
    public static void main(String[] args) { // 메인 메서드: 프로그램 시작 지점
        for (int i = 1; i <= 10; i++) { // 반복문 시작
            if (i == 8) break; // 조건문 시작
            System.out.println("i = " + i); // 콘솔에 메시지 출력
        } // 코드 블록 종료
    } // 코드 블록 종료
} // 코드 블록 종료